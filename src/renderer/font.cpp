// Atlas dimensions
static constexpr u32 ATLAS_COLS = 16;
static constexpr u32 ATLAS_ROWS = 6;
static constexpr u32 ATLAS_CELL_W = GLYPH_W + 1;  // 1px padding
static constexpr u32 ATLAS_CELL_H = GLYPH_H + 1;
static constexpr u32 ATLAS_W = ATLAS_COLS * ATLAS_CELL_W;  // 96 pixels
static constexpr u32 ATLAS_H = ATLAS_ROWS * ATLAS_CELL_H;  // 48 pixels

static u32 s_fontTexture = 0;
static u32 s_textVAO = 0;
static u32 s_textVBO = 0;
static Shader s_textShader;

struct TextVertex {
    Vec3 pos;
    Vec2 uv;
};

static constexpr u32 MAX_TEXT_CHARS = 256;
static constexpr u32 MAX_TEXT_VERTS = MAX_TEXT_CHARS * 6;  // 2 triangles per char

void FontSystem::init() {
    // Generate atlas pixel data
    u8 atlasData[ATLAS_W * ATLAS_H * 4] = {};  // RGBA

    for (u32 charIdx = 0; charIdx < FONT_CHAR_COUNT; charIdx++) {
        u32 col = charIdx % ATLAS_COLS;
        u32 row = charIdx / ATLAS_COLS;
        u32 baseX = col * ATLAS_CELL_W;
        u32 baseY = row * ATLAS_CELL_H;

        for (u32 gy = 0; gy < GLYPH_H; gy++) {
            u8 rowBits = s_glyphData[charIdx][gy];
            for (u32 gx = 0; gx < GLYPH_W; gx++) {
                bool on = (rowBits >> (4 - gx)) & 1;
                u32 px = baseX + gx;
                u32 py = baseY + gy;
                u32 idx = (py * ATLAS_W + px) * 4;
                atlasData[idx + 0] = 255;        // R
                atlasData[idx + 1] = 255;        // G
                atlasData[idx + 2] = 255;        // B
                atlasData[idx + 3] = on ? 255 : 0; // A
            }
        }
    }

    // Upload texture
    glGenTextures(1, &s_fontTexture);
    glBindTexture(GL_TEXTURE_2D, s_fontTexture);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA, ATLAS_W, ATLAS_H, 0, GL_RGBA, GL_UNSIGNED_BYTE, atlasData);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);

    // Create text VAO/VBO
    glGenVertexArrays(1, &s_textVAO);
    glBindVertexArray(s_textVAO);

    glGenBuffers(1, &s_textVBO);
    glBindBuffer(GL_ARRAY_BUFFER, s_textVBO);
    glBufferData(GL_ARRAY_BUFFER, MAX_TEXT_VERTS * sizeof(TextVertex), nullptr, GL_DYNAMIC_DRAW);

    // Position (location 0)
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, sizeof(TextVertex), (void*)0);
    glEnableVertexAttribArray(0);
    // UV (location 2) — matches the unlit shader layout: 0=pos, 1=normal, 2=uv
    glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, sizeof(TextVertex), (void*)sizeof(Vec3));
    glEnableVertexAttribArray(2);

    glBindVertexArray(0);

    // Load unlit shader for text rendering
    s_textShader = ShaderSystem::load(ASSET_PATH("assets/shaders/unlit.vert"), ASSET_PATH("assets/shaders/unlit.frag"));

    LOG_INFO("FontSystem initialized (%ux%u atlas, %u glyphs)", ATLAS_W, ATLAS_H, FONT_CHAR_COUNT);
}

void FontSystem::shutdown() {
    if (s_fontTexture) { glDeleteTextures(1, &s_fontTexture); s_fontTexture = 0; }
    if (s_textVAO)     { glDeleteVertexArrays(1, &s_textVAO); s_textVAO = 0; }
    if (s_textVBO)     { glDeleteBuffers(1, &s_textVBO); s_textVBO = 0; }
    ShaderSystem::destroy(s_textShader);
}

f32 FontSystem::textWidth(const char* text, u32 scale) {
    u32 len = 0;
    while (text[len]) len++;
    return static_cast<f32>(len * (GLYPH_W + GLYPH_SPACING) * scale);
}

f32 FontSystem::textHeight(u32 scale) {
    return static_cast<f32>(GLYPH_H * scale);
}

void FontSystem::drawText(u32 screenWidth, u32 screenHeight,
                          f32 x, f32 y, const char* text,
                          Vec3 color, u32 scale)
{
    if (!s_textVAO || !text || !text[0]) return;

    TextVertex verts[MAX_TEXT_VERTS];
    u32 vertCount = 0;

    f32 curX = x;
    f32 charW   = static_cast<f32>(GLYPH_W * scale);
    f32 charH   = static_cast<f32>(GLYPH_H * scale);
    f32 advance = static_cast<f32>((GLYPH_W + GLYPH_SPACING) * scale);

    f32 invAtlasW = 1.0f / static_cast<f32>(ATLAS_W);
    f32 invAtlasH = 1.0f / static_cast<f32>(ATLAS_H);

    for (const char* p = text; *p && vertCount + 6 <= MAX_TEXT_VERTS; p++) {
        u32 ch = static_cast<u32>(*p);
        if (ch < FONT_FIRST_CHAR || ch > FONT_LAST_CHAR) {
            curX += advance;
            continue;
        }

        u32 glyphIdx = ch - FONT_FIRST_CHAR;
        u32 col = glyphIdx % ATLAS_COLS;
        u32 row = glyphIdx / ATLAS_COLS;

        // UV coordinates in atlas
        f32 u0 = static_cast<f32>(col * ATLAS_CELL_W) * invAtlasW;
        f32 v0 = static_cast<f32>(row * ATLAS_CELL_H) * invAtlasH;
        f32 u1 = static_cast<f32>(col * ATLAS_CELL_W + GLYPH_W) * invAtlasW;
        f32 v1 = static_cast<f32>(row * ATLAS_CELL_H + GLYPH_H) * invAtlasH;

        // Quad corners in screen space
        // y increases upward; v0 is the top of the glyph in the atlas (row origin),
        // so v0 maps to the top of the quad (py1) and v1 maps to the bottom (py0).
        f32 px0 = curX;
        f32 py0 = y;
        f32 px1 = curX + charW;
        f32 py1 = y + charH;

        // Triangle 1: bottom-left, bottom-right, top-right
        verts[vertCount++] = {{px0, py0, 0.0f}, {u0, v1}};
        verts[vertCount++] = {{px1, py0, 0.0f}, {u1, v1}};
        verts[vertCount++] = {{px1, py1, 0.0f}, {u1, v0}};

        // Triangle 2: bottom-left, top-right, top-left
        verts[vertCount++] = {{px0, py0, 0.0f}, {u0, v1}};
        verts[vertCount++] = {{px1, py1, 0.0f}, {u1, v0}};
        verts[vertCount++] = {{px0, py1, 0.0f}, {u0, v0}};

        curX += advance;
    }

    if (vertCount == 0) return;

    // Upload and draw
    glDisable(GL_DEPTH_TEST);
    glEnable(GL_BLEND);
    glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);

    glBindBuffer(GL_ARRAY_BUFFER, s_textVBO);
    glBufferSubData(GL_ARRAY_BUFFER, 0, vertCount * sizeof(TextVertex), verts);

    glUseProgram(s_textShader.program);

    // Orthographic projection: maps pixel coords to NDC
    // Column-major layout matches Mat4 convention in math.h
    Mat4 ortho = Mat4::identity();
    f32 w = static_cast<f32>(screenWidth);
    f32 h = static_cast<f32>(screenHeight);
    ortho.m[0]  =  2.0f / w;
    ortho.m[5]  =  2.0f / h;
    ortho.m[10] = -1.0f;
    ortho.m[12] = -1.0f;
    ortho.m[13] = -1.0f;

    if (s_textShader.loc_mvp >= 0)
        glUniformMatrix4fv(s_textShader.loc_mvp, 1, GL_FALSE, ortho.ptr());

    if (s_textShader.loc_color >= 0)
        glUniform4f(s_textShader.loc_color, color.x, color.y, color.z, 1.0f);

    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, s_fontTexture);
    if (s_textShader.loc_texture0 >= 0)
        glUniform1i(s_textShader.loc_texture0, 0);

    glBindVertexArray(s_textVAO);
    glDrawArrays(GL_TRIANGLES, 0, vertCount);
    glBindVertexArray(0);

    glDisable(GL_BLEND);
    glEnable(GL_DEPTH_TEST);
}
